DROP TABLE Messages CASCADE CONSTRAINTS;
DROP SEQUENCE MsgSeq;

CREATE TABLE Users(
  userID NUMBER PRIMARY KEY,
  name VARCHAR2(40) NOT NULL,
  email VARCHAR2(50) NOT NULL UNIQUE,
  dob TIMESTAMP NOT NULL,
  lastLogin TIMESTAMP
);
CREATE SEQUENCE UsersSEQ
  START WITH 1
  INCREMENT BY 1
  CACHE 100;

CREATE TABLE Friendships(
  senID NUMBER NOT NULL,
  recID NUMBER NOT NULL,
  confirmed NUMBER NOT NULL,
  PRIMARY KEY (senID,recID),
  FOREIGN KEY(senID) references Users(userID),
  FOREIGN KEY(recID) references Users(userID)
);
CREATE TABLE Groups(
  groupID NUMBER PRIMARY KEY,
  name VARCHAR2(30),
  description VARCHAR2(100),
  maxMems NUMBER NOT NULL
);
CREATE SEQUENCE GroupSEQ
  START WITH 1
  INCREMENT BY 1
  CACHE 100;
CREATE TABLE Membership (
  groupID NUMBER NOT NULL,
  userID NUMBER NOT NULL,
  PRIMARY KEY (groupID,userID),
  FOREIGN KEY(groupID) references Groups(groupID),
  FOREIGN KEY(userID) references Users(userID)
);
CREATE Table Messages(
  msgID NUMBER PRIMARY KEY,
  recID NUMBER NOT NULL,
  senID NUMBER NOT NULL,
  subj VARCHAR2(30),
  msgTxt VARCHAR2(100) NOT NULL,
  dateSent TIMESTAMP NOT NULL,
  FOREIGN KEY(recID) references Users(userID),
  FOREIGN KEY(senID) references Users(userID)
);
CREATE SEQUENCE MsgSEQ
  START WITH 1
  INCREMENT BY 1
  CACHE 100;



